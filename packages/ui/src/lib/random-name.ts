/**
 * Generates a random name from the list of adjectives and surnames,
 * formatted as `adjective-surname-nano` (e.g. `focused-turing-xkqj`).
 *
 * @param retry - If > 0, appends a random digit 0-9 after the nano suffix (e.g. …`xkqj3`)
 */
export function randomName(retry = 0): string {
  let core: string;
  do {
    const leftWord = left[Math.floor(Math.random() * left.length)];
    const rightWord = right[Math.floor(Math.random() * right.length)];
    core = `${leftWord}-${rightWord}`;
  } while (core === "boring-wozniak"); // Steve Wozniak is not boring

  let name = `${core}-${randomNano()}`;

  if (retry > 0) {
    name += Math.floor(Math.random() * 10);
  }
  return name;
}
